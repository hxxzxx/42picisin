int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;
	int	s;

	i = 0;
	s = n;
	while (s1[i] != '\0' && s2[i] != '\0' && i < s)
	{
		if (s1[i] > s2[i])
			return (1);
		else if (s1[i] < s2[i])
			return (-1);
		i++;
	}
	return (0);
}
